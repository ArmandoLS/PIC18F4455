    #DEFINE LED LATB, 7
    #DEFINE DATO .27
    #DEFINE CERO B'00000000'
    #DEFINE UNO .1
    #DEFINE DOS .2
    
    ORG .0
    
    
    ;---CONFIGURACION---
   INICIO
    CLRF    PORTA
    MOVLW   B'00110000'
    SETF   TRISA
    
    MOVWF   ADCON1
    MOVLW   .7
    MOVWF   CMCON   
    
    CLRF    LATB
    CLRF    TRISB
    ;---CONFIGURACION---

    MOVFF PORTA, DATO 
    MOVLW B'00110000'
    ANDWF DATO,F
    SWAPF DATO,F	;INTERCAMBIO DE NIBBLES - 4BITS 
    
    MAIN
    
ES_CERO
    
	MOVLW	CERO
	CPFSEQ	DATO
	GOTO	ES_UNO
	GOTO	OFF 
ES_UNO
	MOVLW	UNO
	CPFSEQ	DATO
	GOTO	ES_DOS
	GOTO	ON
	
ES_DOS
	CPFSEQ	DATO
	GOTO	ES_TRES
	GOTO	ON
	
ES_TRES
	GOTO	OFF
	
OFF
	BCF	LED
	GOTO	MAIN
	
ON
	BSF	LED
	GOTO	MAIN 
    
    
    
END